pub fn is_a_reserved_word(word: &str) -> bool {
	for index in 0..RESERVED_WORDS.len() {
		if RESERVED_WORDS[index].starts_with(word) && RESERVED_WORDS[index].ends_with(word) {
			return true;
		}
	}
	return false;
}